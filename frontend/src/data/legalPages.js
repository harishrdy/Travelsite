export function normalizePolicySlug(slug) {
  const value = String(slug || "").trim().toLowerCase();
  if (value === "refund--cancellation-policy") return "refund-cancellation-policy";
  return value;
}

export function ensureCurrentLegalPageContent(pages) {
  const source = Array.isArray(pages) && pages.length > 0 ? pages : DEFAULT_CMS_PAGES;
  const hasTermsPage = source.some((page) => normalizePolicySlug(page?.slug) === "terms-conditions");
  const hasPrivacyPage = source.some((page) => normalizePolicySlug(page?.slug) === "privacy-policy");
  const hasRefundPage = source.some((page) => normalizePolicySlug(page?.slug) === "refund-cancellation-policy");
  const normalizedPages = source.map((page) => {
    const slug = normalizePolicySlug(page?.slug);

    if (slug === "refund-cancellation-policy") {
      return {
        ...DEFAULT_CMS_PAGES[2],
        ...page,
        title: "Refund & Cancellation Policy",
        slug: "refund-cancellation-policy",
        status: page?.status || "Active",
        description: REFUND_CANCELLATION_POLICY_TEXT,
        metaTitle: page?.metaTitle || DEFAULT_CMS_PAGES[2].metaTitle,
        metaKeyword: page?.metaKeyword || DEFAULT_CMS_PAGES[2].metaKeyword,
        metaDescription: page?.metaDescription || DEFAULT_CMS_PAGES[2].metaDescription,
      };
    }

    if (slug === "privacy-policy") {
      return {
        ...DEFAULT_CMS_PAGES[1],
        ...page,
        title: "Privacy Policy",
        slug: "privacy-policy",
        status: page?.status || "Active",
        description: PRIVACY_POLICY_TEXT,
        metaTitle: page?.metaTitle || DEFAULT_CMS_PAGES[1].metaTitle,
        metaKeyword: page?.metaKeyword || DEFAULT_CMS_PAGES[1].metaKeyword,
        metaDescription: page?.metaDescription || DEFAULT_CMS_PAGES[1].metaDescription,
      };
    }

    if (slug !== "terms-conditions") {
      return page;
    }

    return {
      ...DEFAULT_CMS_PAGES[0],
      ...page,
      title: "Terms & Conditions",
      slug: "terms-conditions",
      status: page?.status || "Active",
      description: TERMS_CONDITIONS_TEXT,
      metaTitle: page?.metaTitle || DEFAULT_CMS_PAGES[0].metaTitle,
      metaKeyword: page?.metaKeyword || DEFAULT_CMS_PAGES[0].metaKeyword,
      metaDescription: page?.metaDescription || DEFAULT_CMS_PAGES[0].metaDescription,
    };
  });

  const withTerms = hasTermsPage ? normalizedPages : [DEFAULT_CMS_PAGES[0], ...normalizedPages];
  const withPrivacy = hasPrivacyPage ? withTerms : [...withTerms, DEFAULT_CMS_PAGES[1]];
  return hasRefundPage ? withPrivacy : [...withPrivacy, DEFAULT_CMS_PAGES[2]];
}
